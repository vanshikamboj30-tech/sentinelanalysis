import { useRef } from "react";
import { VideoAnalysis } from "@/types/sentinel";
import VideoPlayer from "./VideoPlayer";
import VideoPlaybackControls from "./VideoPlaybackControls";
import StatsCard from "./StatsCard";
import ChatInterface from "./ChatInterface";
import EventLogs from "./EventLogs";
import SystemHealth from "./SystemHealth";
import AnalyticsCharts from "./AnalyticsCharts";
import { Button } from "@/components/ui/button";
import { Card } from "@/components/ui/card";
import { RotateCcw, FileText, Bell, Mail, Shield, AlertTriangle, CheckCircle2, Brain, Zap } from "lucide-react";
import { toast } from "sonner";
import { api } from "@/lib/api";
import { useSentinelSettings } from "@/hooks/useSentinelSettings";

interface DashboardViewProps {
  data: VideoAnalysis;
  onReset: () => void;
}

const DashboardView = ({ data, onReset }: DashboardViewProps) => {
  const videoRef = useRef<HTMLVideoElement>(null);
  const { settings } = useSentinelSettings();

  const generateReport = () => {
    if (!data.events || data.events.length === 0) {
      toast.error("No detections to report");
      return;
    }

    const reportContent = `
SURVEILLANCE ANALYSIS REPORT (AI-Powered by OpenAI)
Generated: ${new Date().toLocaleString()}
===========================================

${data.stats.overallAssessment ? `AI ASSESSMENT:\n${data.stats.overallAssessment}\n` : ""}
${data.stats.patternInsights?.length ? `\nPATTERN INSIGHTS:\n${data.stats.patternInsights.map((p, i) => `${i + 1}. ${p}`).join("\n")}\n` : ""}
VIDEO INFORMATION:
- Source: ${data.videoUrl}
- Analysis Date: ${new Date().toLocaleDateString()}

SUMMARY STATISTICS:
- Total Detections: ${data.stats.totalDetections}
- High Threat Events: ${data.stats.highThreatEvents}
- Critical Events: ${data.stats.criticalEvents || 0}
- Medium Threat Events: ${data.events.filter((e) => e.threatScore > 40 && e.threatScore <= 70).length}
- Low Threat Events: ${data.events.filter((e) => e.threatScore <= 40).length}

DETECTION LOG:
${data.events
  .map(
    (e, i) =>
      `${i + 1}. [${e.timestamp}] ${e.class.toUpperCase()} - ${e.severity || "N/A"} - Threat: ${e.threatScore}% (Confidence: ${(e.confidence * 100).toFixed(1)}%)${e.explanation ? `\n   AI: ${e.explanation}` : ""}${e.recommendedAction ? `\n   Action: ${e.recommendedAction}` : ""}`
  )
  .join("\n")}

===========================================
Report generated by SENTINEL AI System (OpenAI-powered)
    `.trim();

    const blob = new Blob([reportContent], { type: "text/plain" });
    const url = URL.createObjectURL(blob);
    const a = document.createElement("a");
    a.href = url;
    a.download = `surveillance-analysis-${Date.now()}.txt`;
    document.body.appendChild(a);
    a.click();
    document.body.removeChild(a);
    URL.revokeObjectURL(url);

    toast.success("Report downloaded successfully");
  };

  const setupAlertNotifications = async () => {
    try {
      const highThreatEvents = data.events.filter((e) => e.threatScore >= settings.alertThreshold);
      
      if (highThreatEvents.length === 0) {
        toast.info("No high-threat events detected in this video");
        return;
      }

      await api.sendAlert(highThreatEvents, data.videoUrl, settings.emailRecipient);
      toast.success(`Alert notifications set up for ${highThreatEvents.length} high-threat events`);
    } catch (error) {
      console.error("Alert setup error:", error);
      toast.error("Failed to set up alerts. Make sure the backend is configured.");
    }
  };

  // Calculate threat levels
  const highCount = data.events.filter((e) => e.threatScore >= 70).length;
  const mediumCount = data.events.filter((e) => e.threatScore >= 40 && e.threatScore < 70).length;
  const lowCount = data.events.filter((e) => e.threatScore < 40).length;
  const criticalCount = data.stats.criticalEvents || data.events.filter(e => e.severity === "Critical").length;

  return (
    <div className="space-y-6">
      {/* Header with Status Bar */}
      <div className="flex flex-col lg:flex-row lg:items-center justify-between gap-4">
        <div className="flex items-center gap-4">
          <div className="p-3 rounded-xl bg-gradient-to-br from-primary/20 to-primary/5 border border-primary/30">
            <Shield className="w-6 h-6 text-primary" />
          </div>
          <div>
            <h2 className="text-2xl font-display font-bold text-foreground">
              ANALYSIS COMPLETE
            </h2>
            <div className="flex items-center gap-3 mt-1 flex-wrap">
              {data.aiAnalysis && (
                <span className="text-xs text-primary flex items-center gap-1 bg-primary/10 px-2 py-0.5 rounded-full">
                  <Brain className="w-3 h-3" /> AI-Analyzed
                </span>
              )}
              {data.emailSent && (
                <span className="text-xs text-success flex items-center gap-1 bg-success/10 px-2 py-0.5 rounded-full">
                  <Mail className="w-3 h-3" /> Report emailed
                </span>
              )}
              {data.alertSent && (
                <span className="text-xs text-destructive flex items-center gap-1 bg-destructive/10 px-2 py-0.5 rounded-full">
                  <Bell className="w-3 h-3" /> Alert sent
                </span>
              )}
              <span className="text-xs text-muted-foreground font-mono">
                {data.events.length} DETECTIONS â€¢ {[...new Set(data.events.map(e => e.class))].length} CLASSES
              </span>
            </div>
          </div>
        </div>
        
        <div className="flex items-center gap-3 flex-wrap">
          <SystemHealth />
          <div className="h-6 w-px bg-border hidden md:block" />
          <Button
            onClick={generateReport}
            variant="outline"
            size="sm"
            className="font-display border-border/50 hover:border-primary/50 hover:bg-primary/5"
          >
            <FileText className="w-4 h-4 mr-2" />
            REPORT
          </Button>
          <Button
            onClick={setupAlertNotifications}
            variant="outline"
            size="sm"
            className="font-display border-border/50 hover:border-warning/50 hover:bg-warning/5"
          >
            <Bell className="w-4 h-4 mr-2" />
            ALERTS
          </Button>
          <Button
            onClick={onReset}
            variant="outline"
            size="sm"
            className="font-display border-border/50 hover:border-primary/50 hover:bg-primary/5"
          >
            <RotateCcw className="w-4 h-4 mr-2" />
            NEW
          </Button>
        </div>
      </div>

      {/* AI Overall Assessment */}
      {data.stats.overallAssessment && (
        <Card className="p-4 bg-gradient-to-r from-primary/5 to-transparent border-primary/30">
          <div className="flex items-start gap-3">
            <div className="p-2 rounded-lg bg-primary/20 shrink-0">
              <Brain className="w-5 h-5 text-primary" />
            </div>
            <div className="flex-1">
              <h3 className="text-sm font-display font-semibold text-primary mb-1">
                AI ASSESSMENT
                <span className="text-[10px] font-mono text-muted-foreground ml-2">OpenAI</span>
              </h3>
              <p className="text-sm text-muted-foreground leading-relaxed">
                {data.stats.overallAssessment}
              </p>
              {data.stats.patternInsights && data.stats.patternInsights.length > 0 && (
                <div className="mt-3 flex flex-wrap gap-2">
                  {data.stats.patternInsights.map((insight, i) => (
                    <span key={i} className="text-xs px-2 py-1 rounded-full bg-secondary/50 text-muted-foreground flex items-center gap-1">
                      <Zap className="w-3 h-3 text-warning" />
                      {insight}
                    </span>
                  ))}
                </div>
              )}
            </div>
          </div>
        </Card>
      )}

      {/* Threat Summary Cards */}
      <div className="grid grid-cols-2 md:grid-cols-4 gap-4">
        {criticalCount > 0 && (
          <Card className="p-4 bg-gradient-to-br from-destructive/20 to-transparent border-destructive/50 hover:border-destructive/70 transition-colors">
            <div className="flex items-center gap-3">
              <div className="p-2 rounded-lg bg-destructive/30">
                <Zap className="w-5 h-5 text-destructive" />
              </div>
              <div>
                <p className="text-2xl font-bold font-mono text-destructive">{criticalCount}</p>
                <p className="text-xs text-muted-foreground uppercase tracking-wide">Critical</p>
              </div>
            </div>
          </Card>
        )}
        <Card className="p-4 bg-gradient-to-br from-destructive/10 to-transparent border-destructive/30 hover:border-destructive/50 transition-colors">
          <div className="flex items-center gap-3">
            <div className="p-2 rounded-lg bg-destructive/20">
              <AlertTriangle className="w-5 h-5 text-destructive" />
            </div>
            <div>
              <p className="text-2xl font-bold font-mono text-destructive">{highCount}</p>
              <p className="text-xs text-muted-foreground uppercase tracking-wide">High Threat</p>
            </div>
          </div>
        </Card>
        <Card className="p-4 bg-gradient-to-br from-warning/10 to-transparent border-warning/30 hover:border-warning/50 transition-colors">
          <div className="flex items-center gap-3">
            <div className="p-2 rounded-lg bg-warning/20">
              <Shield className="w-5 h-5 text-warning" />
            </div>
            <div>
              <p className="text-2xl font-bold font-mono text-warning">{mediumCount}</p>
              <p className="text-xs text-muted-foreground uppercase tracking-wide">Medium Threat</p>
            </div>
          </div>
        </Card>
        <Card className="p-4 bg-gradient-to-br from-success/10 to-transparent border-success/30 hover:border-success/50 transition-colors">
          <div className="flex items-center gap-3">
            <div className="p-2 rounded-lg bg-success/20">
              <CheckCircle2 className="w-5 h-5 text-success" />
            </div>
            <div>
              <p className="text-2xl font-bold font-mono text-success">{lowCount}</p>
              <p className="text-xs text-muted-foreground uppercase tracking-wide">Low Threat</p>
            </div>
          </div>
        </Card>
      </div>

      {/* Analytics Charts */}
      <AnalyticsCharts events={data.events} />

      {/* Main Content Grid */}
      <div className="grid grid-cols-1 lg:grid-cols-12 gap-6">
        {/* Video Player Section */}
        <div className="lg:col-span-8 space-y-4">
          <VideoPlayer videoUrl={data.videoUrl} videoRef={videoRef} />
          <VideoPlaybackControls videoRef={videoRef} />
        </div>

        {/* Neural Core Chat */}
        <div className="lg:col-span-4">
          <ChatInterface events={data.events} />
        </div>

        {/* Event Logs - Full Width */}
        <div className="lg:col-span-12">
          <EventLogs events={data.events} />
        </div>
      </div>
    </div>
  );
};

export default DashboardView;
